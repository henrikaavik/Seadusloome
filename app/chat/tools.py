_EXECUTORS: dict[str, Any] = {
    "query_ontology": _exec_query_ontology,
    "search_provisions": _exec_search_provisions,
    "get_draft_impact": _exec_get_draft_impact,
    "get_provision_details": _exec_get_provision_details,
}


async def execute_tool(
    tool_name: str,
    tool_input: dict[str, Any],
    sparql: SparqlClient,
) -> dict[str, Any]:
    """Dispatch a tool call to the appropriate executor.

    Returns a dict with either the tool's results or an ``"error"`` key.
    Executor exceptions are caught and returned as error dicts (never crash).
    """
    fn = _EXECUTORS.get(tool_name)
    if fn is None:
        return {"error": f"Unknown tool: {tool_name}"}
    try:
        return await fn(tool_input, sparql)
    except Exception as e:
        logger.exception("Tool %s failed", tool_name)
        return {"error": str(e)[:200]}
